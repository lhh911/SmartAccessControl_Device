package com.xsjqzt.module_main.model;

public class CardBean {
    /**
     * sn : 1234567890
     * id : 1138
     * name : 劳启
     */

    private String sn;
    private int id;
    private String name;

    public String getSn() {
        return sn;
    }

    public void setSn(String sn) {
        this.sn = sn;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
